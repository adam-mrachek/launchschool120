# Which of the following are objects in Ruby? If they are objects, how can you find out what class they belong to?

true
"hello"
[1, 2, 3, "happy days"]
142

# Yes, they are all objects in Ruby. You can find out what class by calling `.class` on each one.

p true.class
p "hello".class
p [1, 2, 3, "happy days"].class
p 142.class